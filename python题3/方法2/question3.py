count = 0
for i in range(1, 20):  # 假如全买公鸡最多20个
    for j in range(1, 33):  # 假如全买母鸡最多33个
        for k in range(3, 99, 3):  # 假如全买小鸡最多98个，并且小鸡的数量必须是3的倍数，要保证全部都有
            if i + j + k == 100 and i * 5 + j * 3 + k // 3 == 100:  # 判断公鸡母鸡小鸡是否有100只 和 加起来是否为100文钱
                count += 1  # 方案数加1
                print("方案", count, "：公鸡", i, "只，母鸡", j, "只,小鸡", k, "只。")

print("一共", count, "种方案。")
